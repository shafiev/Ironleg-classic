  CREATE OR REPLACE FUNCTION "IRONLEG"."BALANCE" (s_id integer)
return number as
    first_payment_date date;
    sql_q varchar2(1000);
    balance_d number;
    price number;
    all_payments number;
    next_month date;
begin
    sql_q := 'select min(payment_date)
                  from clients_payments
                  where client_id = :1';
    EXECUTE IMMEDIATE sql_q into first_payment_date using s_id;
    next_month := first_payment_date + to_char(last_day(first_payment_date),'DD');
    balance_d := 0;
        while next_month <= sysdate loop
            begin
                dbms_output.put_line(next_month);
                sql_q := 'select price
                              from tarifs_history
                              where (tarif_id,tarif_date) = (select th.tarif_id,max(th.tarif_date)
                                                                          from ats_eq_ports aep inner join tarifs_history th on aep.tarif_id = TH.TARIF_ID
                                                                          where tarif_date < :1
                                                                          and school_id = :2
                                                                          group by th.tarif_id
                                                                         )';
                EXECUTE IMMEDIATE sql_q into price using next_month,s_id;
                next_month := next_month + to_char(last_day(next_month),'DD');
                balance_d := balance_d + price;
            end;
        end loop;
    sql_q := 'select sum(amount)
                  from clients_payments
                  where client_id = :1';
    EXECUTE IMMEDIATE sql_q into all_payments using s_id;
    balance_d := all_payments - balance_d;
    return balance_d;    
exception
    when others then
    dbms_output.put_line(SQLERRM);
end;


